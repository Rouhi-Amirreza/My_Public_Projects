import React from 'react';
import {
  View,
  Text,
  StyleSheet,
  ScrollView,
  TouchableOpacity,
  Alert,
  Share,
  Dimensions,
} from 'react-native';
import { GeneratedItinerary, MultiDayItinerary, DailyItinerary, BookingInfo } from '../types';

const { width } = Dimensions.get('window');

interface PrintItineraryProps {
  itinerary: GeneratedItinerary | MultiDayItinerary;
  bookingInfo: BookingInfo;
  startingLocation: { address: string };
  returnLocation: { address: string };
  onClose: () => void;
}

const PrintItinerary: React.FC<PrintItineraryProps> = ({
  itinerary,
  bookingInfo,
  startingLocation,
  returnLocation,
  onClose,
}) => {
  const isMultiDay = 'dailyItineraries' in itinerary;

  const formatTime = (timeString: string) => {
    const [hours, minutes] = timeString.split(':');
    const hour = parseInt(hours);
    const ampm = hour >= 12 ? 'PM' : 'AM';
    const formattedHour = hour % 12 || 12;
    return `${formattedHour}:${minutes} ${ampm}`;
  };

  const handlePrint = async () => {
    try {
      const itineraryText = generateItineraryText();
      await Share.share({
        message: itineraryText,
        title: 'My Travel Itinerary',
      });
    } catch (error) {
      Alert.alert('Error', 'Failed to share itinerary');
    }
  };

  const generateItineraryText = () => {
    let text = `üóìÔ∏è MY TRAVEL ITINERARY\n\n`;
    text += `üìç Starting Location: ${startingLocation.address}\n`;
    text += `üìç Return Location: ${returnLocation.address}\n\n`;

    if (isMultiDay) {
      const multiDayItinerary = itinerary as MultiDayItinerary;
      text += `üìÖ Duration: ${multiDayItinerary.totalDays} days\n`;
      text += `üìç Total Places: ${multiDayItinerary.totalPlaces}\n\n`;

      multiDayItinerary.dailyItineraries.forEach((dailyItinerary, index) => {
        text += `DAY ${index + 1} - ${dailyItinerary.date}\n`;
        text += `‚è∞ ${formatTime(dailyItinerary.startTime)} - ${dailyItinerary.availableHours} hours available\n\n`;
        
        dailyItinerary.schedule.forEach((scheduleItem, scheduleIndex) => {
          text += `${scheduleIndex + 1}. ${scheduleItem.place.name}\n`;
          text += `   üìç ${scheduleItem.place.address}\n`;
          text += `   ‚è∞ ${formatTime(scheduleItem.arrivalTime)} - ${formatTime(scheduleItem.departureTime)}\n`;
          text += `   ‚åõ ${scheduleItem.visitDuration} minutes\n`;
          if (scheduleItem.place.rating) {
            text += `   ‚≠ê ${scheduleItem.place.rating}/5\n`;
          }
          text += `\n`;
        });
        text += `\n`;
      });
    } else {
      const singleDayItinerary = itinerary as GeneratedItinerary;
      text += `üìç Total Places: ${singleDayItinerary.places.length}\n\n`;
      
      singleDayItinerary.schedule.forEach((scheduleItem, index) => {
        text += `${index + 1}. ${scheduleItem.place.name}\n`;
        text += `   üìç ${scheduleItem.place.address}\n`;
        text += `   ‚è∞ ${formatTime(scheduleItem.arrivalTime)} - ${formatTime(scheduleItem.departureTime)}\n`;
        text += `   ‚åõ ${scheduleItem.visitDuration} minutes\n`;
        if (scheduleItem.place.rating) {
          text += `   ‚≠ê ${scheduleItem.place.rating}/5\n`;
        }
        text += `\n`;
      });
    }

    if (bookingInfo.ticketBookings.length > 0) {
      text += `üé´ TICKET BOOKINGS:\n\n`;
      bookingInfo.ticketBookings.forEach((ticket, index) => {
        text += `${index + 1}. ${ticket.placeName}\n`;
        text += `   Type: ${ticket.ticketType}\n`;
        text += `   Quantity: ${ticket.quantity}\n`;
        text += `   Price: $${ticket.totalPrice.toFixed(2)}\n\n`;
      });
    }

    if (bookingInfo.rideBookings.length > 0) {
      text += `üöó RIDE BOOKINGS:\n\n`;
      bookingInfo.rideBookings.forEach((ride, index) => {
        text += `${index + 1}. ${ride.rideType.toUpperCase()}\n`;
        text += `   Route: ${ride.fromLocation} ‚Üí ${ride.toLocation}\n`;
        text += `   Distance: ${Math.round(ride.distance / 1000 * 10) / 10} km\n`;
        text += `   Duration: ${ride.estimatedDuration} minutes\n`;
        text += `   Price: $${ride.estimatedPrice.toFixed(2)}\n\n`;
      });
    }

    if (bookingInfo.totalCost > 0) {
      text += `üí∞ TOTAL COST: $${bookingInfo.totalCost.toFixed(2)}\n`;
      text += `   Tickets: $${bookingInfo.totalTicketCost.toFixed(2)}\n`;
      text += `   Rides: $${bookingInfo.totalRideCost.toFixed(2)}\n\n`;
    }

    text += `Generated by NaviNook Travel Planner üß≠`;
    return text;
  };

  const renderDailyItinerary = (dailyItinerary: DailyItinerary, dayIndex: number) => (
    <View key={dayIndex} style={styles.daySection}>
      <Text style={styles.dayTitle}>Day {dayIndex + 1} - {dailyItinerary.date || 'Unknown Date'}</Text>
      <Text style={styles.daySubtitle}>
        {formatTime(dailyItinerary.startTime || '00:00')} ‚Ä¢ {dailyItinerary.availableHours || 0} hours available
      </Text>
      
      {dailyItinerary.schedule?.map((scheduleItem, index) => (
        <View key={index} style={styles.placeItem}>
          <View style={styles.placeHeader}>
            <Text style={styles.placeNumber}>{index + 1}</Text>
            <View style={styles.placeInfo}>
              <Text style={styles.placeName}>{scheduleItem.place?.name || 'Unknown Place'}</Text>
              <Text style={styles.placeAddress}>{scheduleItem.place?.address || 'No address available'}</Text>
            </View>
          </View>
          <View style={styles.placeDetails}>
            <Text style={styles.placeTime}>
              {formatTime(scheduleItem.arrivalTime || '00:00')} - {formatTime(scheduleItem.departureTime || '00:00')}
            </Text>
            <Text style={styles.placeDuration}>
              {scheduleItem.visitDuration || 0} minutes
            </Text>
            {scheduleItem.place?.rating && (
              <Text style={styles.placeRating}>
                ‚≠ê {scheduleItem.place.rating}/5
              </Text>
            )}
          </View>
        </View>
      ))}
    </View>
  );

  const renderSingleDayItinerary = () => {
    const singleDayItinerary = itinerary as GeneratedItinerary;
    return (
      <View style={styles.daySection}>
        <Text style={styles.dayTitle}>Your Itinerary</Text>
        <Text style={styles.daySubtitle}>
          {singleDayItinerary.places?.length || 0} places ‚Ä¢ {Math.round((singleDayItinerary.totalDuration || 0) / 60)} hours
        </Text>
        
        {singleDayItinerary.schedule?.map((scheduleItem, index) => (
          <View key={index} style={styles.placeItem}>
            <View style={styles.placeHeader}>
              <Text style={styles.placeNumber}>{index + 1}</Text>
              <View style={styles.placeInfo}>
                <Text style={styles.placeName}>{scheduleItem.place?.name || 'Unknown Place'}</Text>
                <Text style={styles.placeAddress}>{scheduleItem.place?.address || 'No address available'}</Text>
              </View>
            </View>
            <View style={styles.placeDetails}>
              <Text style={styles.placeTime}>
                {formatTime(scheduleItem.arrivalTime || '00:00')} - {formatTime(scheduleItem.departureTime || '00:00')}
              </Text>
              <Text style={styles.placeDuration}>
                {scheduleItem.visitDuration || 0} minutes
              </Text>
              {scheduleItem.place?.rating && (
                <Text style={styles.placeRating}>
                  ‚≠ê {scheduleItem.place.rating}/5
                </Text>
              )}
            </View>
          </View>
        ))}
      </View>
    );
  };

  return (
    <View style={styles.container}>
      <View style={styles.header}>
        <Text style={styles.headerTitle}>üìÑ Print Itinerary</Text>
        <TouchableOpacity style={styles.closeButton} onPress={onClose}>
          <Text style={styles.closeButtonText}>√ó</Text>
        </TouchableOpacity>
      </View>

      <ScrollView style={styles.content} showsVerticalScrollIndicator={false}>
        <View style={styles.itineraryHeader}>
          <Text style={styles.itineraryTitle}>üóìÔ∏è My Travel Itinerary</Text>
          <View style={styles.locationInfo}>
            <Text style={styles.locationLabel}>Starting Location:</Text>
            <Text style={styles.locationText}>{startingLocation?.address || 'No starting location'}</Text>
          </View>
          <View style={styles.locationInfo}>
            <Text style={styles.locationLabel}>Return Location:</Text>
            <Text style={styles.locationText}>{returnLocation?.address || 'No return location'}</Text>
          </View>
        </View>

        {isMultiDay ? (
          (itinerary as MultiDayItinerary).dailyItineraries.map(renderDailyItinerary)
        ) : (
          renderSingleDayItinerary()
        )}

        {bookingInfo?.ticketBookings?.length > 0 && (
          <View style={styles.bookingSection}>
            <Text style={styles.bookingSectionTitle}>üé´ Ticket Bookings</Text>
            {bookingInfo.ticketBookings.map((ticket, index) => (
              <View key={index} style={styles.bookingItem}>
                <Text style={styles.bookingTitle}>{ticket?.placeName || 'Unknown Place'}</Text>
                <Text style={styles.bookingDetails}>
                  {ticket?.ticketType || 'Unknown Type'} ‚Ä¢ Qty: {ticket?.quantity || 0} ‚Ä¢ ${(ticket?.totalPrice || 0).toFixed(2)}
                </Text>
              </View>
            ))}
          </View>
        )}

        {bookingInfo?.rideBookings?.length > 0 && (
          <View style={styles.bookingSection}>
            <Text style={styles.bookingSectionTitle}>üöó Ride Bookings</Text>
            {bookingInfo.rideBookings.map((ride, index) => (
              <View key={index} style={styles.bookingItem}>
                <Text style={styles.bookingTitle}>{ride?.rideType?.toUpperCase() || 'UNKNOWN'}</Text>
                <Text style={styles.bookingDetails}>
                  {ride?.fromLocation || 'Unknown'} ‚Üí {ride?.toLocation || 'Unknown'}
                </Text>
                <Text style={styles.bookingDetails}>
                  {Math.round((ride?.distance || 0) / 1000 * 10) / 10} km ‚Ä¢ {ride?.estimatedDuration || 0} min ‚Ä¢ ${(ride?.estimatedPrice || 0).toFixed(2)}
                </Text>
              </View>
            ))}
          </View>
        )}

        {bookingInfo?.totalCost > 0 && (
          <View style={styles.totalSection}>
            <Text style={styles.totalTitle}>üí∞ Total Cost</Text>
            <View style={styles.totalRow}>
              <Text style={styles.totalLabel}>Tickets:</Text>
              <Text style={styles.totalValue}>${(bookingInfo?.totalTicketCost || 0).toFixed(2)}</Text>
            </View>
            <View style={styles.totalRow}>
              <Text style={styles.totalLabel}>Rides:</Text>
              <Text style={styles.totalValue}>${(bookingInfo?.totalRideCost || 0).toFixed(2)}</Text>
            </View>
            <View style={styles.totalDivider} />
            <View style={styles.totalRow}>
              <Text style={styles.totalGrandLabel}>Total:</Text>
              <Text style={styles.totalGrandValue}>${(bookingInfo?.totalCost || 0).toFixed(2)}</Text>
            </View>
          </View>
        )}

        <View style={styles.footer}>
          <Text style={styles.footerText}>Generated by NaviNook Travel Planner üß≠</Text>
        </View>
      </ScrollView>

      <View style={styles.actionButtons}>
        <TouchableOpacity style={styles.printButton} onPress={handlePrint}>
          <Text style={styles.printButtonText}>üì§ Share Itinerary</Text>
        </TouchableOpacity>
      </View>
    </View>
  );
};

